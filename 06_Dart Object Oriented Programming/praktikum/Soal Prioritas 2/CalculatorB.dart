// struktur class kalkulator pertama yang tidak menggunakan property dan constructor
class CalculatorB {
  // method menerima parameter angka secara langsung
  double add(double x, double y) => x + y; // penjumlahan
  double subtract(double x, double y) => x - y; // pengurangan
  double multiply(double x, double y) => x * y; // perkalian
  double divide(double x, double y) => x / y; // pembagian
}
